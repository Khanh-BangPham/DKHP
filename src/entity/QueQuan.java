package entity;

/**
 *
 * @author PC
 */
public class QueQuan {
    private int maQQ;
    private String tenQQ;

    public QueQuan() {
    }

    public QueQuan(int maQQ, String tenQQ) {
        this.maQQ = maQQ;
        this.tenQQ = tenQQ;
    }

    public int getMaQQ() {
        return maQQ;
    }

    public void setMaQQ(int maQQ) {
        this.maQQ = maQQ;
    }

    public String getTenQQ() {
        return tenQQ;
    }

    public void setTenQQ(String tenQQ) {
        this.tenQQ = tenQQ;
    }
    
}
