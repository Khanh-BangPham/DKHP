package entity;

public class HocKy {
    private int maHocKy;
    private String hocKy;

    public HocKy() {
    }

    public HocKy(int maHocKy, String hocKy) {
        this.maHocKy = maHocKy;
        this.hocKy = hocKy;
    }

    public int getMaHocKy() {
        return maHocKy;
    }

    public void setMaHocKy(int maHocKy) {
        this.maHocKy = maHocKy;
    }

    public String getHocKy() {
        return hocKy;
    }

    public void setHocKy(String hocKy) {
        this.hocKy = hocKy;
    }
    
    
}
