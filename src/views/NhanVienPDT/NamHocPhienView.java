    private void btnDeleteNHHKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteNHHKActionPerformed
        // TODO add your handling code here:
        CT_HocKy_NamHoc_DAO cthknhdao = new CT_HocKy_NamHoc_DAO();
        int maCTHKNH = -1;
        try {
            maCTHKNH = cthknhdao.getCT_HocKy_NamHocByNameNH_HK(comboBoxHocKyNHHK.getSelectedItem().toString(), comboBoxNamHocNHHK.getSelectedItem().toString()).getId_cthknh();

            if(cthknhdao.checkCT_HocKy_NamHocAndLTC(maCTHKNH) > 0){
                resetModelHKNH();
               JOptionPane.showMessageDialog(this, "Không thể Học Kỳ - Năm Học Đã có Lớp Tín Chỉ!");
            } else {
                if(cthknhdao.delCT_HocKy_NamHocToDB(maCTHKNH) > 0){
                    cthknhdao = new CT_HocKy_NamHoc_DAO();
                    fillDataTableNHHK(cthknhdao.getListCT_HocKy_NamHoc());
                    resetModelHKNH();
                   JOptionPane.showMessageDialog(this, "Xóa thành công Học Kỳ - Năm Học!");
               } else {
                   JOptionPane.showMessageDialog(this, "Xóa không thành công Học Kỳ - Năm Học!");
               }                
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnDeleteNHHKActionPerformed

