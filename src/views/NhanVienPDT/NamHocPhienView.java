    public void fillDataTableNHHK(List<CT_HocKy_NamHoc> listCTHKNH){
        NamHocDAO nhdao = new NamHocDAO();
        HocKyDAO hkdao = new HocKyDAO();
        GiaTinChiDAO gtcdao = new GiaTinChiDAO();
        int i = 1;
        DefaultTableModel model = (DefaultTableModel) tableNHHK.getModel();
        model.setRowCount(0);
        for(CT_HocKy_NamHoc item : listCTHKNH){
            Object dataRow[] = new Object[14];
            dataRow[0] = i++;
            dataRow[1] = nhdao.getNamHocByID(item.getMaNamHoc()).getTenNamHoc();
            dataRow[2] = hkdao.getHocKyByID(item.getMaHocKy()).getHocKy();
            dataRow[3] = item.getNgayBatDau();
            dataRow[4] = item.getNgayKetThuc();
            dataRow[5] = gtcdao.getGiaTinChiByID(item.getIdgtc()).getGiaTien() + "000 VND";
            model.addRow(dataRow);
        }
        model.fireTableDataChanged();
    }
        public void fillDataTablePhien(List<Phien> listphien){
        NamHocDAO nhdao = new NamHocDAO();
        HocKyDAO hkdao = new HocKyDAO();
        CT_HocKy_NamHoc_DAO ctnhhkdao = new CT_HocKy_NamHoc_DAO();
        int i = 1;
        DefaultTableModel model = (DefaultTableModel) tablePhien.getModel();
        model.setRowCount(0);
        for(Phien item : listphien){
            Object dataRow[] = new Object[14];
            dataRow[0] = i++;
            dataRow[1] = nhdao.getNamHocByID(ctnhhkdao.getCT_HocKy_NamHocByID(item.getId_cthknh()).getMaNamHoc()).getTenNamHoc();
            dataRow[2] = hkdao.getHocKyByID(ctnhhkdao.getCT_HocKy_NamHocByID(item.getId_cthknh()).getMaHocKy()).getHocKy();
            dataRow[3] = item.getNgayBD();
            dataRow[4] = item.getNgayKT();
            model.addRow(dataRow);
        }
        model.fireTableDataChanged();
    }
