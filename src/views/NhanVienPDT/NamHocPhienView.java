    private void btnEditNHHKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditNHHKActionPerformed
        // TODO add your handling code here:
         NamHocDAO nhdao = new NamHocDAO();
        HocKyDAO hkdao = new HocKyDAO();
        GiaTinChiDAO gtcdao = new GiaTinChiDAO();
        CT_HocKy_NamHoc_DAO cthknhdao = new CT_HocKy_NamHoc_DAO();
        CT_HocKy_NamHoc ctnhhk = new CT_HocKy_NamHoc();
        
        try {
            ctnhhk.setMaNamHoc(nhdao.getNamHocByName(comboBoxNamHocNHHK.getSelectedItem().toString().strip()).getMaNamHoc());
           ctnhhk.setMaHocKy(hkdao.getHocKyByName(comboBoxHocKyNHHK.getSelectedItem().toString().strip()).getMaHocKy());
           SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
           Date ngayBDNHHK = null;
           Date ngayKTNHHK = null;
           try {
               ngayBDNHHK = formatter.parse(txtNgayBatDau.getText());
               ngayKTNHHK = formatter.parse(txtNgayKetThuc.getText());
           } catch (Exception e) {
               e.printStackTrace();
           }
           ctnhhk.setNgayBatDau(ngayBDNHHK);
           ctnhhk.setNgayKetThuc(ngayKTNHHK);
           ctnhhk.setIdgtc(gtcdao.mergeGiaTinChiByGiaTien(Integer.parseInt(txtGiaTC.getText())));
           if(cthknhdao.editCT_HocKy_NamHocToDB(ctnhhk) > 0){
               fillDataTableNHHK(cthknhdao.getListCT_HocKy_NamHoc());
               JOptionPane.showMessageDialog(this, "Thêm thành công năm học!");
           } else {
               JOptionPane.showMessageDialog(this, "Thêm không thành công năm học!");
           }
           
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnEditNHHKActionPerformed

