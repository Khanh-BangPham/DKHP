    private boolean isValidForm(){
        if(Integer.parseInt(txtSoTinChi.getText().strip()) < 2 && Integer.parseInt(txtSoTinChi.getText().strip()) > 4){
            JOptionPane.showMessageDialog(this, "Số tín chỉ không bé hơn 2 và không lớn hơn 4!");
            return false;
        }
        return true;
    }
    public MonHoc getModel(){
        MonHoc mh = new MonHoc();
        HocKyDAO hkdao = new HocKyDAO();
        mh.setMaMonHoc(txtMaMH.getText().strip());
        mh.setTenMonhoc(txtTenMonHoc.getText().strip());
        mh.setSoTicChi(Integer.parseInt(txtSoTinChi.getText().strip()));
        return mh;
    }
    public void setModel(MonHoc mh){
        HocKyDAO hkdao = new HocKyDAO();
        txtMaMH.setText(mh.getMaMonHoc());
        txtTenMonHoc.setText(mh.getTenMonhoc());
        txtSoTinChi.setText(Integer.toString(mh.getSoTicChi()));
    }
    public void resetModel(){
        txtMaMH.setText("");
        txtTenMonHoc.setText("");
        txtSoTinChi.setText("");
    } 
    
