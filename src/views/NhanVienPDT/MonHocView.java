    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        MonHocDAO mhdao = new MonHocDAO();
        HocKyDAO hkdao = new HocKyDAO();
        List<MonHoc> listAllMh = mhdao.getListMonHoc();
        List<MonHoc> listFilteredMh = new ArrayList<MonHoc>();
        if(txtMaMH.getText().strip().equals("") 
                && txtTenMonHoc.getText().strip().equals("")
                && txtSoTinChi.getText().strip().equals("")
        ){
            
            JOptionPane.showMessageDialog(this, "Nhập thông tin môn học để tìm kiếm!");
        } else {
            for(MonHoc mh: listAllMh){
                if( mh.getMaMonHoc().contains(txtMaMH.getText().strip())
                && mh.getTenMonhoc().contains(txtTenMonHoc.getText().strip())
                && (txtSoTinChi.getText().strip().equals("") ? true  : mh.getSoTicChi() == Integer.parseInt(txtSoTinChi.getText().strip()))
                ){
                    listFilteredMh.add(mh);
                } 
            }
            fillDataTable(listFilteredMh);
        }        
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        MonHocDAO mhdao = new MonHocDAO();
        int confirm = JOptionPane.showConfirmDialog(null,
            "Bạn có chắc chắn muốn xóa?", "Xóa!", JOptionPane.DEFAULT_OPTION);
        // 0=yes, 1=no, 2=cancel
        if(confirm == 0){
            if(mhdao.checkMonHocLopTinChiByID(txtMaMH.getText().strip()) == -1){
                JOptionPane.showMessageDialog(this, "Môn học đã có lớp tín chỉ, không thể xóa!");
            } else {
                
                if(mhdao.delMonHocByID(txtMaMH.getText().strip()) > 0){
                    JOptionPane.showMessageDialog(this, "Xóa thành công sinh viên!");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa không thành công sinh viên!");
                }              
            }

        }        
    }//GEN-LAST:event_btnDeleteActionPerformed

