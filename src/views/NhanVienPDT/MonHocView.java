    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        MonHocDAO mhdao = new MonHocDAO();
        List<MonHoc> listmh = null;
        MonHoc mh = getModel();
        try {
            if(isValidForm()){
                if(mhdao.addMonHocToDB(mh) > 0){
                    mhdao = new MonHocDAO();
                    listmh = mhdao.getListMonHoc();
                    fillDataTable(listmh);
                    JOptionPane.showMessageDialog(this, "Thêm thành công Môn học!");
                    resetModel();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm không thành công Môn học!");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnInsertActionPerformed

