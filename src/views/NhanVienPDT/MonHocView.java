    public void fillDataTable(List<MonHoc> listmh){        
        HocKyDAO hkdao = new HocKyDAO();
        List<HocKy> listhk = hkdao.getListHocKy();
        int i = 1;
        DefaultTableModel model = (DefaultTableModel) tableMonHoc.getModel();
        model.setRowCount(0);
        for(MonHoc mh : listmh){
            Object dataRow[] = new Object[12];
            dataRow[0] = i++;
            dataRow[1] = mh.getMaMonHoc();
            dataRow[2] = mh.getTenMonhoc();
            dataRow[3] = mh.getSoTicChi();
            model.addRow(dataRow);
        }
        model.fireTableDataChanged();
    }    
